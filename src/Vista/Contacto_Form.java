    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (jTextField1.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debes ingresar un ID de Contacto... ");
        } else {

            int id_Contacto = Integer.parseInt(jTextField1.getText());
            Contacto r = Transaccion_Contacto.buscarContacto(id_Contacto);

            if (r == null) {
                javax.swing.JOptionPane.showMessageDialog(this, "No se encontro el Contacto... ");
                limpiaCampos();

            } else {
                muevaloalaventana(r);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty() 
            || jTextField5.getText().isEmpty() || jTextField6.getText().isEmpty() || jTextField7.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debes completar los datos... ");
        } else {

            Contacto x = null;
            x = armecontactodelaVentana();
            Transaccion_Contacto.insertaContacto(x);
            limpiaCampos();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

