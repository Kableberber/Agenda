    private void contactoID(){
        
        Contacto Y = Transaccion_Contacto.buscarContactoID_Contacto();

        if (Y == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "No se encontraron contactos... ");
        } else {
        jTextField2.setText(Integer.toString(Y.getID_Contacto()+1));
        }
        
    }
    
