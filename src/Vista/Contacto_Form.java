    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        if (jTextField1.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debes buscar un contacto... ");
        } else {

            int confirmado = javax.swing.JOptionPane.showConfirmDialog(this, "¿Estas seguro?");

            if (javax.swing.JOptionPane.OK_OPTION == confirmado) {
                int id_contacto = Integer.parseInt(jTextField2.getText());
                Transaccion_Contacto.borrarContacto(id_contacto);
                limpiaCampos();
            } else {
                System.out.println("Cancelado por el usuario...");
            }


        }
    }//GEN-LAST:event_jButton4ActionPerformed

